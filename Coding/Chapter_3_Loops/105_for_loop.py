#examples for for loop

#example 1
fruits = ['Apple','Orange','Watermelon']
for fruit in fruits:
    print(fruit)

#example 2

for num in range(10):
    print(num)

print('\n')

#example 3
for num in range(10,20):
    print(num)

print('\n')

#example 4
for num in range(10,20,2):
    print(num)

print('\n')

#example 4
name = 'John Smith'
for char in name:
    print(char)